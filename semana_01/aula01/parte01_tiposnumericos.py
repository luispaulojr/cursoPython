"""
Operações aritméticas e tipos
"""

# declaração e uma variavel
num = 42

# imprimindo o valor da variavel
print(num)

# soma
print(num + 2)

# divisão
print(num / 2)

# inteiro da divisão
print(num // 2)

# resto da divisão
print(num % 2)

# potencia
print(num ** 2)

# numero mágico
print(2 ** 63)

# capacidade do python depende da capacidade de processamento da máquina
print(2 ** 1000)

# o python faz cast implicito

valor1 = 1.44 + 3
print(valor1)

# converter float em inteiro
print(int(valor1))